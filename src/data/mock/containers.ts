export function generateMockContainers(): Container[] {
	// Generate more containers to reach 1666 total for pagination testing
	const allContainers = [...mockContainers]
	const baseContainers = [...mockContainers]

	// Generate variations to reach total count
	for (let i = 0; i < 87; i++) {
		baseContainers.forEach((container, idx) => {
			if (allContainers.length >= 1666) return

			allContainers.push({
				...container,
				slug: `${container.slug}-${i}-${idx}`,
				name: `${container.name}-${i}-${idx}`,
				last_changed: new Date(Date.now() - Math.random() * 30 * 24 * 60 * 60 * 1000).toISOString()
			})
		})
	}

	return allContainers.slice(0, 1666)
}
